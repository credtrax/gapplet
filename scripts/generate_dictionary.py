def main() -> None:
    words = set()
    for token in RAW_WORDS.split():
        word = token.strip().upper()
        if word and word.isalpha():
            words.add(word)
    sorted_words = sorted(words)
    with open("src/data/words.txt", "w") as f:
        for w in sorted_words:
            f.write(w + "\n")
    print(f"Wrote {len(sorted_words)} words to src/data/words.txt")


if __name__ == "__main__":
    main()
